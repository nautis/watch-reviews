// menu locations

function ocdi_after_import_setup() {
  // Assign menus to their locations.
    $main_menu = get_term_by( 'name', 'Primary Menu', 'nav_menu' );
    $split_menu_left = get_term_by( 'name', 'Split menu Left', 'nav_menu' );
    $split_menu_right = get_term_by( 'name', 'Split Menu Right', 'nav_menu' );
    $subscribe = get_term_by( 'name', 'Subscribe', 'nav_menu' );
    $footer = get_term_by( 'name', 'Footer', 'nav_menu' );

    set_theme_mod( 'nav_menu_locations', [
            'primary' => $main_menu->term_id, // primary
            'split-menu-left' => $split_menu_left->term_id, // split menu left
            'split-menu-right' => $split_menu_right->term_id, // split menu right
            'slide-menu-primary' => $main_menu->term_id, // secondary header (subscribe)
            'header-secondary' => $subscribe->term_id, // footer
            'footer' => $footer->term_id, // footer
        ]
    );

}
add_action( 'ocdi/after_import', 'ocdi_after_import_setup' );